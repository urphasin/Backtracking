package STRUCTURES;

public class TreeNode {
    TreeNode left;
    TreeNode right;
    int value;

    public TreeNode(int value) {
        this.left = null;
        this.right= null;
        this.value = value;
    }
    public TreeNode() {
        this.left = null;
        this.right= null;
        this.value = 0;
    }


}
